-- 2. BRANCH Table
CREATE TABLE BRANCH(
    bank_ID NUMERIC,
    branch_ID NUMERIC PRIMARY KEY,
    branch_name VARCHAR(20) NOT NULL,
    branch_address VARCHAR(20),
    FOREIGN KEY(bank_ID) REFERENCES BANK(bank_ID)
);

