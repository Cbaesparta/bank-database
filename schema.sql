-- 3. ACCOUNT Table
CREATE TABLE ACCOUNT(
    account_ID NUMERIC PRIMARY KEY,
    branch_ID NUMERIC,
    name VARCHAR(20) NOT NULL,
    account_type VARCHAR(20) NOT NULL,
    FOREIGN KEY(branch_ID) REFERENCES BRANCH(branch_ID)
);

