-- 4. LOAN Table
CREATE TABLE LOAN(
    account_ID NUMERIC,
    loan_ID NUMERIC PRIMARY KEY,
    name VARCHAR(20) NOT NULL,
    loan_type VARCHAR(20) NOT NULL,
    loan_amount NUMERIC,
    FOREIGN KEY(account_ID) REFERENCES ACCOUNT(account_ID)
);

